        r"(?P<prefix>\bIoCreateDevice\s*\([^;]*?,\s*)0x100(?:u|U)?(?P<suffix>\s*,\s*FALSE\s*,)",
        r"\g<prefix>FILE_DEVICE_SECURE_OPEN\g<suffix>",
